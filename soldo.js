let db;
const request = indexedDB.open("SoldoDB", 2);

request.onupgradeneeded = function (event) {
  db = event.target.result;
  const store = db.createObjectStore("soldos", {
    keyPath: "id",
    autoIncrement: true,
  });
  store.createIndex("mesAnoGradPosto", ["mesAno", "gradPosto"], {
    unique: true,
  });
};

request.onsuccess = function (event) {
  db = event.target.result;
  clearDatabase(addSoldos);
};

request.onerror = function (event) {
  console.log("Erro ao abrir o banco de dados:", event.target.errorCode);
};

function clearDatabase(callback) {
  const transaction = db.transaction(["soldos"], "readwrite");
  const store = transaction.objectStore("soldos");
  const clearRequest = store.clear();

  clearRequest.onsuccess = function () {
    console.log("Base de dados limpa com sucesso.");
    callback(soldos);
  };

  clearRequest.onerror = function () {
    console.log("Erro ao limpar a base de dados:", clearRequest.error);
  };
}

function addSoldos(soldos) {
  const transaction = db.transaction(["soldos"], "readwrite");
  const store = transaction.objectStore("soldos");

  soldos.forEach((soldo) => {
    const mesAnoGradPosto = [soldo[0], soldo[1]];

    const getRequest = store.index("mesAnoGradPosto").get(mesAnoGradPosto);
    getRequest.onsuccess = function () {
      if (!getRequest.result) {
        const request = store.add({
          mesAno: soldo[0],
          gradPosto: soldo[1],
          valor: parseFloat(soldo[2]).toFixed(2),
        });

        request.onerror = function () {
          console.log("Erro ao adicionar soldo:", request.error);
        };
      } else {
        console.log(`Soldo já existe: ${soldo[1]}, ${soldo[0]}`);
      }
    };

    getRequest.onerror = function () {
      console.log("Erro ao verificar soldo existente:", getRequest.error);
    };
  });
}

