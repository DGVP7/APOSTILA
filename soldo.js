// Função para buscar soldo baseado em gradPosto e mesAno
function buscarSoldo(
  gradPosto,
  mesAno,
  inatividade,
  anosDeServico,
  reforma,
  exOficio,
) {
  console.log("Iniciando a busca de soldo...");
  console.log(
    `Parâmetros: gradPosto=${gradPosto}, mesAno=${mesAno}, inatividade=${inatividade}, anosDeServico=${Math.trunc(anosDeServico)}, reforma=${reforma}, exOficio=${exOficio}`,
  );

  return new Promise((resolve, reject) => {
    // Define a categoria de soldo com base nos critérios
    let categoria = "soldo";
    const dataReferencia = new Date("2022-01-01");
    const dataExclusao = new Date(
      `20${mesAno.split("/")[1]}-${mesAno.split("/")[0]}-01`,
    );

    if (
      inatividade === "RESERVA REMUNERADA" &&
      dataExclusao >= dataReferencia
    ) {
      categoria = "soldo";
    } else if (
      inatividade === "RESERVA REMUNERADA" &&
      dataExclusao < dataReferencia
    ) {
      categoria = "soldo acima";
    } else if (
      inatividade === "RESERVA REMUNERADA EX-OFFÍCIO" &&
      exOficio === "por Idade"
    ) {
      if (dataExclusao < dataReferencia && anosDeServico > 30) {
        categoria = "soldo acima";
      } else if (dataExclusao < dataReferencia && anosDeServico <= 30) {
        categoria = "soldo";
      } else if (dataExclusao >= dataReferencia) {
        categoria = "soldo";
      }
    } else if (
      inatividade === "RESERVA REMUNERADA EX-OFFÍCIO" &&
      exOficio === "Cargo Eletivo"
    ) {
      if (anosDeServico > 30) {
        categoria = "soldo acima";
      } else {
        categoria = "soldo proporcional";
      }
    } else if (
      inatividade === "REFORMA" &&
      reforma === "Art. 107, I, II, III, IV."
    ) {
      categoria = "soldo 107";
    } else if (inatividade === "REFORMA" && reforma === "Art. 107, V.") {
      categoria = "soldo proporcional";
    } else if (
      inatividade === "REFORMA" &&
      reforma === "Art. 107, V c/ Art. 110, I"
    ) {
      categoria = "soldo";
    }

    console.log(`Categoria definida: ${categoria}`);

    if (categoria === "soldo") {
      buscarSoldoBase(gradPosto, mesAno).then(resolve).catch(reject);
    } else if (categoria === "soldo acima") {
      buscarSoldoAcima(gradPosto, mesAno).then(resolve).catch(reject);
    } else if (categoria === "soldo 107") {
      buscarSoldo107(gradPosto, mesAno).then(resolve).catch(reject);
    } else if (categoria === "soldo proporcional") {
      buscarSoldoProporcional(gradPosto, Math.floor(anosDeServico), mesAno)
        .then(resolve)
        .catch(reject);
    } else {
      reject("Categoria de soldo não reconhecida.");
    }
  });
}

// Função para buscar o soldo base
function buscarSoldoBase(gradPosto, mesAno) {
  return new Promise((resolve, reject) => {
    const transaction = db.transaction(["soldos"], "readonly");
    const store = transaction.objectStore("soldos");
    const index = store.index("mesAnoGradPosto");
    const request = index.get([mesAno, gradPosto]);

    request.onsuccess = function (event) {
      if (event.target.result) {
        resolve({ valor: event.target.result.valor, gradPosto });
      } else {
        reject("Soldo não encontrado.");
      }
    };

    request.onerror = function () {
      reject("Erro ao buscar soldo.");
    };
  });
}

// Função para buscar soldo acima
function buscarSoldoAcima(gradPosto, mesAno) {
  const gradPostos = [
    "Soldado BM",
    "Cabo BM",
    "3º Sargento BM",
    "2º Sargento BM",
    "1º Sargento BM",
    "Subtenente BM",
    "2º Tenente BM",
    "1º Tenente BM",
    "Capitão BM",
    "Major BM",
    "Ten. Cel. BM",
    "Coronel BM",
  ];

  let novoGradPosto = gradPosto;
  if (gradPosto === "Coronel BM") {
    novoGradPosto = "Coronel BM+20%";
  } else {
    const index = gradPostos.indexOf(gradPosto);
    if (index !== -1 && index < gradPostos.length - 1) {
      novoGradPosto = gradPostos[index + 1];
    }
  }

  return buscarSoldoBase(novoGradPosto, mesAno);
}

// Função para buscar soldo 107
function buscarSoldo107(gradPosto, mesAno) {
  const gradPostos107 = {
    "Subtenente BM": "1º Tenente BM",
    "1º Sargento BM": "2º Tenente BM",
    "2º Sargento BM": "2º Tenente BM",
    "3º Sargento BM": "2º Tenente BM",
    "Cabo BM": "3º Sargento BM",
    "Soldado BM": "3º Sargento BM",
  };

  const novoGradPosto = gradPostos107[gradPosto] || gradPosto;
  return buscarSoldoBase(novoGradPosto, mesAno);
}

// Função para buscar soldo proporcional
function buscarSoldoProporcional(gradPosto, anosDeServico, mesAno) {
  return new Promise((resolve, reject) => {
    buscarSoldoBase(gradPosto, mesAno)
      .then((resultado) => {
        const valor = parseFloat(resultado.valor);
        const anosDeServicoInteiros = Math.trunc(anosDeServico);
        const soldoProporcional = (valor / 30) * anosDeServicoInteiros;
        resolve({ valor: soldoProporcional.toFixed(2), gradPosto });
      })
      .catch(reject);
  });
}

